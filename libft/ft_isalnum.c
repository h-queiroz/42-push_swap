int	ft_isalnum(int c)
{
	unsigned int	result;

	result = 0;
	if ((c >= 'a' && c <= 'z') || \
	(c >= 'A' && c <= 'Z') || \
	(c >= '0' && c <= '9'))
		result = 1;
	return (result);
}
